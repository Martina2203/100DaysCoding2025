package day26;

public class Day26 {

    public static void main(String[] args) {
        // Operator penugasan (=,+=,-=,dll)
        int a = 10;
        System.out.println("Nilai 1 = " + a);
        
        a += 5;
        System.out.println("Nilai 2 = " + a);
        
        a -= 8;
        System.out.println("Nilai 3 = " + a);
        
        a *= 5;
        System.out.println("Nilai 4 = " + a);
        
        a /= 5;
        System.out.println("Nilai 5 = " + a); 
        
        a %= 6;
        System.out.println("Nilai 6 = " + a);
        
    }
    
}
