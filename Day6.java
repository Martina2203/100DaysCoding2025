package day6;

public class Day6 {
    
    public static void main(String[] args) {
        // Ukuran 4 byte (32 bit) 
        float berat = 33.2f;
        
        // Ukuran 8 byte (64 bit)
        double tinggi = 144.2;
      
        
        // Menampilkan hasil output
        System.out.println("Berat badan saya yaitu " + berat + "kg");
        System.out.println("Tinggi badan saya sekitar " + tinggi + "cm");
        
        /* 
         Perbedaan dari float dan double yaitu ukuran penyimpanan double lebih besar dari pada
         float dan juga kita harus menambahkan huruf f dibelakang angka float contoh 33.2f
        */
        
    }
    
}
