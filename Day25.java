package day25;

public class Day25 {

    public static void main(String[] args) {
        // Operator Increment dan Descrement (++,--)
        int a1, a2, b1, b2;
        a1 = 6;
        a2 = 8;
        b1 = 5;
        b2 = 7;

        System.out.println("Post Increment: " + a1++); 

        System.out.println("Pre Increment: " + ++a2); 

        System.out.println("Post Descrement: " + b1--); 

        System.out.println("Pre Descrement: " + --b2); 

    }
    
}
