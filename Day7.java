package day7;

public class Day7 {

    public static void main(String[] args) {
        // Untuk menyimpan 1 karakter tunggal
        char nilai = 'A';
        
        // Untuk menyimpan 2 nilai logika true dan false
        boolean lulus = true;
        
        // Tampilan hasil outputnya
        System.out.println("Nilai saya: " + nilai);
        System.out.println("Apakah lulus? " + lulus);
        
        /*
        Perbedaannya yaitu penulisan char menggunakan tanda petik tunggal sedangkan 
        boolean ditulis langsung. char bisa menampung jenis karaktek dari A-Z, angka, simbol,
        unicode, dll dan boolean hanya true dan false.
        */
        
    }
    
}
